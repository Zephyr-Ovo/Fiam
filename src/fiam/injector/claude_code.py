"""
Claude Code injector — writes pre-session output into the AI's home.

Writes to:
  - home_path/recall.md      (synthesis output, updated every pre_session)
  - home_path/CLAUDE.md      (context-setting, written only on first run)
  - home_path/.gitignore             (ignore Obsidian + auto-generated files, first run)
"""

from __future__ import annotations

from datetime import datetime, timezone
from pathlib import Path

from fiam.config import FiamConfig


_CLAUDE_MD_INITIAL = """\
这是你的家。

recall.md 是你涌现的回忆，每次对话前更新。
self/ 是你的私人空间：
  - self/personality.md 是你对自己的描述，想写什么写什么。
  - self/journal/ 是你想写东西的地方，没有格式要求。

git 是你自己的版本历史，不是某个项目的代码仓库。
`git log` 是你回看自己的方式。
"""


_GITIGNORE_INITIAL = """\
# Obsidian workspace cache
.obsidian/workspace.json
.obsidian/workspace-mobile.json

# OS artifacts
.DS_Store
Thumbs.db

# Auto-generated by fiam (regenerated every session)
recall.md
"""


def write_background(config: FiamConfig, synthesis_text: str) -> Path:
    """Write the synthesis output to recall.md.

    The synthesis_text already contains its own header (<!-- fiam synthesis ... -->).
    We only add the timestamp comment.
    """
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    content = (
        f"<!-- last updated: {timestamp} -->\n"
        f"\n"
        f"{synthesis_text.strip()}\n"
    )
    path = config.background_path
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return path


def write_claude_md(config: FiamConfig) -> Path | None:
    """Write CLAUDE.md only if it doesn't already exist.

    Returns the path if written, None if file already existed.
    """
    path = config.claude_md_path
    if path.exists():
        return None
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(_CLAUDE_MD_INITIAL, encoding="utf-8")
    return path


def write_gitignore(config: FiamConfig) -> Path | None:
    """Write .gitignore only if it doesn't already exist."""
    path = config.home_path / ".gitignore"
    if path.exists():
        return None
    path.write_text(_GITIGNORE_INITIAL, encoding="utf-8")
    return path


def write(config: FiamConfig, synthesis_text: str) -> dict[str, str | None]:
    """Combined injector entry point.

    Returns dict with paths written (or None if skipped).
    """
    bg_path = write_background(config, synthesis_text)
    claude_path = write_claude_md(config)
    gitignore_path = write_gitignore(config)
    return {
        "background_path": str(bg_path),
        "claude_md_path": str(claude_path) if claude_path else None,
        "gitignore_path": str(gitignore_path) if gitignore_path else None,
    }
